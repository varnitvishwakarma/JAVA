class HelloWorld {
    public static void main(String[] args) {
     //LOOPS

     //FOR LOOP

     for(int i=100;i>=1;i--){
     System.out.println(i);
    }

     //WHILE LOOP

     int j = 10;
     while(j>=1){
        System.out.println(j);
        j--;
     }

     //DO WHILE LOOP

     int k =100;
     do{
        System.out.println(k);
        k--;
     } while(k>=1);




       
 }
}

    

    
